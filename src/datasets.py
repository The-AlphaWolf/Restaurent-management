"""
Dataset registry.

The project supports two interchangeable data sources that share the same
canonical schema, so the feature/model/dashboard code is dataset-agnostic:

- ``synthetic``: a 3-year simulation generated by ``data_generation.py``.
- ``real``: real daily restaurant visitor counts (Recruit Restaurant, Japan),
  prepared by ``prepare_real_data.py``.

Canonical schema (``*_sales.csv``): at minimum ``date``, ``item_id``,
``units_sold``; optionally ``category`` and ``is_holiday``.
Menu schema (``*_menu.csv``): ``item_id``, ``category``, ``name`` and the INR
cost columns ``selling_price`` and ``food_cost``.
"""

from __future__ import annotations

import os

# Absolute path to the repository root (this file lives in ``<root>/src``).
REPO_ROOT = os.path.dirname(os.path.dirname(os.path.abspath(__file__)))

DATASETS: dict[str, dict] = {
    "synthetic": {
        "label": "Synthetic (3-year simulation)",
        "sales": "data/sales_data.csv",
        "menu": "data/menu_items.csv",
        "model": "models/best_model.pkl",
        # Fixed cutoff -> last ~6 months are the test set.
        "split_date": "2023-07-01",
        "test_fraction": None,
    },
    "real": {
        "label": "Real (Recruit Restaurant, Japan)",
        "sales": "data/real_sales_data.csv",
        "menu": "data/real_menu_items.csv",
        "model": "models/best_model_real.pkl",
        # Real data spans a different window -> split by the last 20% of dates.
        "split_date": None,
        "test_fraction": 0.2,
    },
}


def resolve(path: str) -> str:
    """Turn a repo-relative path from the registry into an absolute path."""
    return os.path.join(REPO_ROOT, path)


def get_config(source: str) -> dict:
    """Return the registry entry for ``source`` (``synthetic`` or ``real``)."""
    if source not in DATASETS:
        raise ValueError(f"Unknown dataset source '{source}'. Choose from {list(DATASETS)}.")
    return DATASETS[source]
